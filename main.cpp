/**
 * Быстрое возведение в степень для остаточного члена ряда Тейлора.
 * Вычисляет (1 + s)^power - 1.
 * ВАЖНО: power обязательно должно быть степенью двойки (например, 512).
 */
dd_ fast_taylor_pow(dd_ s, int power)
{
    // Пока степень больше 1, применяем формулу (1+s)^2 - 1 = 2s + s^2
    while (power > 1) {
        s = mul_pwr2(s, 2.0) + squre_two(s);
        power >>= 1;
    }
    return s;
}

dd_ exp( const dd_ &a)
{
    /**
     * Редуцируем экспоненту, для того чтобы ряд тейлора быстрее сходился,
     * и затем считаем всю функцию
     */

    const int k = 512.0;                            // Обязательно k = 2^p
    const double limit_exp_arg = 709.0;

   /**
     * Что за число 709.
     * Заметим, что максимальное значение числа double — 2^1024. Если мы прологарифмируем
     * это число, то получим значение, близкое к 709. То есть оно является верхней границей,
     * после которой произойдет переполнение (overflow) или уход в бесконечность.
     *
     * Соответственно, в зависимости от знака, это будет либо бесконечность, либо ноль.
     */

    /**
     * Почему именно k = 512.
     * Мы представляем начальное число a в виде: a = m * ln(2) + k * r.
     * Тогда exp(a) = exp(m * ln(2)) * exp(r)^k = 2^m * exp(r)^k.
     *
     * Максимальное значение a (709) здесь совершенно ни при чем.
     * Выбор k = 512 (то есть 2^9) — это оптимизация баланса между скоростью
     * сходимости ряда Тейлора и стоимостью возведения в степень.
     *
     * 1. После вычитания m * ln(2) остаток лежит в пределах [-ln(2)/2, ln(2)/2].
     *    Разделив его на k = 512, мы получаем настолько малое значение r, что ряд
     *    Тейлора для exp(r) сходится до нужной нам точности всего за ~15 итераций.
     * 2. Выбор именно степени двойки (k = 2^9) позволяет вычислить exp(r)^512
     *    максимально эффективно: для этого достаточно ровно 9 раз последовательно
     *    возвести результат в квадрат (что и делается в конце функции).
     */

    double k_inv = 1.0 / k;

    if (a.hi <= -limit_exp_arg) return 0.0;
    if (a.hi >= limit_exp_arg) return Consts::INF;

    if (a.hi == 0) return 1.0;
    if (a.hi == 1.0 && a.lo == 0.0) return Consts::EXP;

    /**
     * EXP(a) = EXP(k*r + m * ln(2)) = 2^m * EXP(r)^k
     */

    double m = std::floor(a.hi / Consts::LN2.hi + 0.5);    // + 0.5 необходим для правильного округления.
    dd_ r = mul_pwr2(a -  Consts::LN2 * m , k_inv);

    dd_ s, t, p;

    p = squre_two(r);

    s = r + mul_pwr2(p, 0.5);
    p = p * r;

    t = p * Consts::factorial[0];

    int i = 0;
    do {
        s = s + t;
        p = p * r;

        i++;

        t = p * Consts::factorial[i];
    } while (std::abs(t.hi) > k_inv * Consts::EPS && i < 15);

    s = s + t;

    // Возводим в степень.

    /**
     * Так как мы нашли s = exp(r) - 1, то теперь мы хотим при помощи алгоритма быстрого
     * возведения в степень получить exp(r)^512.
     * Формула: (1 + s)^2 - 1 = 1 + 2s + s^2 - 1 = 2s + s^2.
     */

    s = fast_taylor_pow(s, k);

    s += 1.0;

    return ldexp(s, static_cast<int>(m));
}

int main() {

    dd_ x(154.3456456, 0.0);
    dd_ res = exp(x);

    // Устанавливаем точность вывода
    std::cout.precision(17);
    std::cout << "e^1 = " << res.hi << " (hi)" << std::endl;
    std::cout << "err = " << res.lo << " (lo)" << std::endl;

    // Сравним с библиотечным значением
    std::cout << "std::exp(1) = " << std::exp(154.3456456) << std::endl;

    return 0;
}